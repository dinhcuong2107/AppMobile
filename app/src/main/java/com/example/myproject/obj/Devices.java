package com.example.myproject.obj;

public class Devices {
    public String img1;
    public String img2;
    public String img3;
    public String company;
    public String name;
    public String cpu;
    public String ram;
    public String rom;
    public String price;
    public String sl;
    public String type;
    public String color1;
    public String color2;
    public String color3;
    public String detail;
    public Devices() {
    }

    public Devices(String img1, String img2, String img3, String company, String name, String cpu, String ram, String rom, String price, String sl, String type, String color1, String color2, String color3, String detail) {
        this.img1 = img1;
        this.img2 = img2;
        this.img3 = img3;
        this.company = company;
        this.name = name;
        this.cpu = cpu;
        this.ram = ram;
        this.rom = rom;
        this.price = price;
        this.sl = sl;
        this.type = type;
        this.color1 = color1;
        this.color2 = color2;
        this.color3 = color3;
        this.detail = detail;
    }
}
